char	*ft_strchr(const char *s, int c)
{
	int		i;
	char	d;

	d = (char)c;
	i = 0;
	while (s[i] != d)
	{
		if (s[i] == '\0' && s[i] != d)
			return (0);
		i++;
	}
	return ((char *)(s + i));
}
